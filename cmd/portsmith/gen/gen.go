// Package gen implements the portsmith gen command.
package gen

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/miilkaa/portsmith/internal/gen"
	"golang.org/x/tools/imports"
)

// Run executes the gen command with the given arguments.
func Run(args []string) error {
	dryRun := false
	all := false
	var dirs []string

	for _, a := range args {
		switch a {
		case "--dry-run":
			dryRun = true
		case "--all":
			all = true
		default:
			dirs = append(dirs, a)
		}
	}

	if all {
		matches, _ := filepath.Glob("internal/*")
		for _, m := range matches {
			fi, err := os.Stat(m)
			if err != nil || !fi.IsDir() {
				continue
			}
			if hasFiles(m, "handler.go", "service.go", "repository.go") {
				dirs = append(dirs, m)
			}
		}
		sort.Strings(dirs)
	}

	if len(dirs) == 0 {
		return fmt.Errorf("no package directories specified (use --all or provide paths)")
	}

	for _, d := range dirs {
		if err := genPackage(d, dryRun); err != nil {
			return fmt.Errorf("%s: %w", d, err)
		}
	}
	return nil
}

func genPackage(dir string, dryRun bool) error {
	base := filepath.Base(dir)
	prefix := gen.PortPrefix(base)
	repoPort := prefix + "Repository"
	svcPort := prefix + "Service"

	pkgName, err := gen.PackageName(filepath.Join(dir, "service.go"))
	if err != nil {
		return err
	}

	sources, err := gatherSources(dir)
	if err != nil {
		return err
	}

	repoMethods := make(map[string]struct{})
	svcMethods := make(map[string]struct{})
	for _, body := range sources {
		repoMethods = gen.Union(repoMethods, gen.CollectRepoCalls(body))
		svcMethods = gen.Union(svcMethods, gen.CollectServiceCalls(body))
	}

	pkg, err := gen.ParsePackage(dir)
	if err != nil {
		return err
	}

	repoSigs := gen.MethodSigs(pkg, "Repository")
	svcSigs := gen.MethodSigs(pkg, "Service")

	var buf bytes.Buffer
	fmt.Fprintf(&buf, "// Code generated by portsmith gen; DO NOT EDIT.\n\npackage %s\n\n", pkgName)

	fmt.Fprintf(&buf, "// %s is the repository surface used by Service and Handler.\n", repoPort)
	fmt.Fprintf(&buf, "type %s interface {\n", repoPort)
	for _, name := range gen.SortSet(repoMethods) {
		sig, ok := repoSigs[name]
		if !ok {
			return fmt.Errorf("repository method %q called but not found on *Repository", name)
		}
		fmt.Fprintf(&buf, "\t%s\n", sig)
	}
	fmt.Fprintf(&buf, "}\n\n")
	fmt.Fprintf(&buf, "var _ %s = (*Repository)(nil)\n\n", repoPort)

	fmt.Fprintf(&buf, "// %s is the service surface used by Handler.\n", svcPort)
	fmt.Fprintf(&buf, "type %s interface {\n", svcPort)
	for _, name := range gen.SortSet(svcMethods) {
		sig, ok := svcSigs[name]
		if !ok {
			return fmt.Errorf("service method %q called from handler but not found on *Service", name)
		}
		fmt.Fprintf(&buf, "\t%s\n", sig)
	}
	fmt.Fprintf(&buf, "}\n\n")
	fmt.Fprintf(&buf, "var _ %s = (*Service)(nil)\n", svcPort)

	out, err := imports.Process("ports.go", buf.Bytes(), nil)
	if err != nil {
		return fmt.Errorf("format: %w\n%s", err, buf.String())
	}

	if dryRun {
		fmt.Printf("=== %s/ports.go ===\n%s\n", dir, out)
		return nil
	}

	target := filepath.Join(dir, "ports.go")
	return os.WriteFile(target, out, 0o644)
}

func gatherSources(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, err
	}
	var bodies []string
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		n := e.Name()
		if !strings.HasSuffix(n, ".go") || strings.HasSuffix(n, "_test.go") || n == "ports.go" {
			continue
		}
		if n == "adapters.go" || strings.HasSuffix(n, "_adapter.go") {
			continue
		}
		b, err := os.ReadFile(filepath.Join(dir, n))
		if err != nil {
			return nil, err
		}
		bodies = append(bodies, string(b))
	}
	return bodies, nil
}

func hasFiles(dir string, names ...string) bool {
	for _, n := range names {
		if _, err := os.Stat(filepath.Join(dir, n)); err != nil {
			return false
		}
	}
	return true
}
