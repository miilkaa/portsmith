package gen

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"

	"github.com/miilkaa/portsmith/internal/analyze"
	"github.com/miilkaa/portsmith/internal/project"
	"golang.org/x/tools/go/packages"
	"golang.org/x/tools/imports"
)

func genPackage(dir string, cfg project.Config, dryRun, scanCallers bool, modulePath string, modulePkgs []*packages.Package) (string, error) {
	base := filepath.Base(dir)

	pkgName, err := analyze.PackageName(filepath.Join(dir, "service.go"))
	if err != nil {
		return "", err
	}

	sources, err := analyze.LoadSources(dir)
	if err != nil {
		return "", err
	}

	repoMethods := make(map[string]struct{})
	svcMethods := make(map[string]struct{})
	for _, body := range sources {
		repoMethods = analyze.Union(repoMethods, analyze.CollectRepoCallsWithAllowed(body, cfg.Lint.CallPatterns.Service.Allowed))
		svcMethods = analyze.Union(svcMethods, analyze.CollectServiceCallsWithAllowed(body, cfg.Lint.CallPatterns.Handler.Allowed))
	}

	if scanCallers && modulePath != "" {
		// dir may come in as "./internal/bots" or "internal/bots". Clean it to
		// avoid producing a malformed import path with embedded "./".
		relDir := filepath.ToSlash(filepath.Clean(dir))
		targetImportPath := modulePath + "/" + relDir
		extraRepo, extraSvc := analyze.CollectCrossModuleCalls(modulePkgs, targetImportPath)
		repoMethods = analyze.Union(repoMethods, extraRepo)
		svcMethods = analyze.Union(svcMethods, extraSvc)
	}

	pkg, err := analyze.ParsePackage(dir)
	if err != nil {
		return "", err
	}

	prefix := analyze.PortPrefix(base)
	if p, ok := analyze.InferPortPrefix(pkg); ok {
		prefix = p
	}
	repoPort := prefix + "Repository"
	svcPort := prefix + "Service"

	repoSigs := analyze.MethodSigs(pkg, "Repository")
	svcSigs := analyze.MethodSigs(pkg, "Service")

	var buf bytes.Buffer
	// bytes.Buffer writes never fail; discard the error centrally to keep the linter quiet.
	emit := func(format string, args ...any) {
		_, _ = fmt.Fprintf(&buf, format, args...)
	}

	emit("// Code generated by portsmith gen; DO NOT EDIT.\n\npackage %s\n\n", pkgName)

	emit("// %s is the repository surface used by Service and Handler.\n", repoPort)
	emit("type %s interface {\n", repoPort)
	for _, name := range analyze.SortSet(repoMethods) {
		sig, ok := repoSigs[name]
		if !ok {
			return "", fmt.Errorf("repository method %q called but not found on *Repository", name)
		}
		emit("\t%s\n", sig)
	}
	emit("}\n\n")
	emit("var _ %s = (*Repository)(nil)\n\n", repoPort)

	emit("// %s is the service surface used by Handler.\n", svcPort)
	emit("type %s interface {\n", svcPort)
	for _, name := range analyze.SortSet(svcMethods) {
		sig, ok := svcSigs[name]
		if !ok {
			return "", fmt.Errorf("service method %q called from handler but not found on *Service", name)
		}
		emit("\t%s\n", sig)
	}
	emit("}\n\n")
	emit("var _ %s = (*Service)(nil)\n", svcPort)

	out, err := imports.Process("ports.go", buf.Bytes(), nil)
	if err != nil {
		return "", fmt.Errorf("format: %w\n%s", err, buf.String())
	}

	if dryRun {
		return fmt.Sprintf("=== %s/ports.go ===\n%s\n", dir, out), nil
	}

	target := filepath.Join(dir, "ports.go")
	return "", os.WriteFile(target, out, 0o644)
}
